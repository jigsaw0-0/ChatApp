import Foundation
import UIKit

extension UIColor {
    
    class var MKOutgoingBubbleReply : UIColor {
        
        return UIColor.init(named: "MK_OutgoingReply") ?? UIColor.black
    }
    
    class var MKIncomingBubbleReply : UIColor {
        
        return UIColor.init(named: "MK_IncomingReply") ?? UIColor.black
    }
    
    
    
}

